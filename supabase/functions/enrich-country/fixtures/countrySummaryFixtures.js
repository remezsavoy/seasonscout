function createSourceContext({
  source,
  query,
  title,
  description,
  leadParagraph,
  supportingParagraphs,
  revisionId,
}) {
  const cleanedParagraphs = [leadParagraph, ...(supportingParagraphs ?? [])].filter(Boolean);

  return {
    source,
    success: true,
    query,
    cleanedText: cleanedParagraphs.join(' '),
    description,
    excerptText: leadParagraph,
    leadParagraph,
    supportingParagraphs: supportingParagraphs ?? [],
    selectedPage: {
      id: null,
      key: title?.toLowerCase().replace(/\s+/g, '-') ?? null,
      title: title ?? null,
      matchedTitle: title ?? null,
      description: description ?? null,
      thumbnailUrl: null,
    },
    revisionId: revisionId ?? null,
    warning: null,
  };
}

